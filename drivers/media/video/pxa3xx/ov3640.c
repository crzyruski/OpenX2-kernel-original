int read_sensor_reg_16bit(u16 addr, u8 *pvalue)
{    
	u8 data;        
	u8 address[2];
	if( g_client == NULL )	/* Not initialize the client */	
	{
		ovI2C_dbg("not initialize the client\n");
		return -1;
	}
	address[0] = addr>>8;	
	address[1] = addr;
	i2c_smbus_write_byte_data(g_client,address[0],address[1]);
	data = i2c_smbus_read_byte(g_client);
	*pvalue = data;   

	return 0;
}
int write_sensor_reg_16bit(u16 addr, u8 *value)
{
	u8 data[3];
	if( g_client == NULL )	/* Not initialize the client */
		return -1;			
	data[0] = addr>>8;
	data[1] = addr;	
	data[2]=  *value;
	

	i2c_master_send(g_client, data, 3);
	
	return 0;
}

int write_sensor_reg_16bit_m(u16 addr, u8 value, u8 mask)
{
	int ret = 0;
	u8 regval;
	
	read_sensor_reg_16bit(addr, &regval);
	
	regval  &=  (~mask);
	value   &=  mask;
	
	regval|=value;
	
	ret = write_sensor_reg_16bit(addr,&regval);

	return ret;
}

