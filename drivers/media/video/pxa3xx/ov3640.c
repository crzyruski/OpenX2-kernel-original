int ov3640_record_previewcode(void)
{
	u8 reg;
	write_sensor_reg(0x33a0,0);//awb r
	read_sensor_reg(0x33ca,&ov3640_recRgain);

	write_sensor_reg(0x33a0,1);//awb g
	read_sensor_reg(0x33ca,&ov3640_recGgain);


	
	write_sensor_reg(0x33a0,2);//awb b
	read_sensor_reg(0x33ca,&ov3640_recBgain);	

	read_sensor_reg(0x3002,&reg);	
	ov3640_expcode = (int)reg;
	ov3640_expcode <<= 8;
	read_sensor_reg(0x3003,&reg);	
	ov3640_expcode += reg;
	read_sensor_reg(0x3001,&ov3640_gaincode);
	return 0;
}

