int ov3640_set_regs(const u16 *regP)
{
	u32	curReg = 0;
	u8 	regVal = 0;
	int	status = 0;
	/* The list is a register number followed by the value */
	while(regP[curReg << 1] != OV3640_REGEND)
	{
		regVal = regP[(curReg << 1) + 1];
		status = (write_sensor_reg( regP[curReg << 1], regVal) == 0) ?
			0 : -EIO;
		curReg++;
	}

	return status;
}

